for a=1:length(Path_M243_basal)
    cd(Path_M243_basal{a})
    res=pwd;
    
    % define 5 successive Sleep epochs
    load StateEpochSB SWSEpoch REMEpoch
    load behavResources
    startRecord=tpsdeb{1}*1E4;
    endRecord=tpsfin{end}*1E4;
    EpochRecord=intervalSet(startRecord,endRecord);
    EpochSWSRecord=and(EpochRecord,SWSEpoch);
    
    for i=1:12
        RealHour(i)=((8+i)*3600)*1E4;
    end
    TimeDebRecord=(TimeDebRec(1,3)+TimeDebRec(1,2)*60+TimeDebRec(1,1)*3600)*1E4;
    TimeEndRecord=(TimeEndRec(end,3)+TimeEndRec(end,2)*60+TimeEndRec(end,1)*3600)*1E4;
    
    c=max(find(TimeDebRecord>RealHour(:))); % find when start recording compared to real hours
    d=max(find(TimeEndRecord>RealHour(:))); % find when start recording compared to real hours
    
    b=1;
    for i=1:5
        EpSleepA{i}=intervalSet(RealHour(b)-TimeDebRecord,RealHour(b+2)-TimeDebRecord);     % 5 epoch 8-10h / 10-12h / 12-14h / 14-16h / 16-18h
        b=b+1;
    end
        
    for i=1:5
        EpochSleep{i}=and(EpSleepA{i},EpochSWSRecord);
    end
    
    
    % restrict Delta occurence to the 5 succesive Sleep epochs
    load([res,'/newDelta',struc]);
    for i=1:5
        Delta_M243_basal{a,i}=Restrict(ts(tDelta),EpochSleep{i});
        DurDeltaEpoch_M243_basal{a,i}=tot_length(EpochSleep{i},'s');
    end
end

